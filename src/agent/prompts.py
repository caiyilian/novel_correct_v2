# ─── Prompt 选择器 ──────────────────────────────────────

_TYPE_PROMPT_MAP = {
    "consecutive": ("连续符号连续", CONSECUTIVE_PROMPT),
    "unpaired": ("不成对符号", UNPAIRED_PROMPT),
    "wrong_symbol": ("非标准符号", WRONG_SYMBOL_PROMPT),
    "long_dialogue": ("超长对话", LONG_DIALOGUE_PROMPT),
    "missing_bracket": ("缺失符号", MISSING_BRACKET_PROMPT),
}


def build_user_prompt(error: ErrorRecord) -> str:
    """
    为指定错误构建 User Prompt（含专用提示）。
    """
    type_name, type_prompt = _TYPE_PROMPT_MAP.get(
        error.error_type,
        ("未知类型", ""),
    )

    msg = f"## 当前错误\n\n"
    msg += f"错误 ID: {error.error_id}\n"
    msg += f"错误类型: {type_name} ({error.error_type})\n"
    msg += f"位置: 第{error.line_number}行 (offset {error.offset})\n"
    msg += f"错误内容: {error.original_text[:100]}\n\n"

    if error.context_before:
        msg += f"上文: ...{error.context_before[-80:]}\n\n"
    if error.context_after:
        msg += f"下文: {error.context_after[:80]}...\n\n"

    if type_prompt:
        msg += f"## 专用判断规则\n\n{type_prompt}\n\n"

    msg += "## 操作\n"
    msg += "请使用工具阅读上下文并处理此错误。"
    return msg


# ─── 主构建函数 ─────────────────────────────────────────

def build_system_prompt(error_type: str) -> str:
    """
    根据错误类型构建完整的 System Prompt。
    通用规则 + 错误类型专用提示。
    """
    _, type_prompt = _TYPE_PROMPT_MAP.get(error_type, ("", ""))
    if type_prompt:
        return BASE_SYSTEM_PROMPT + "\n\n" + type_prompt
    return BASE_SYSTEM_PROMPT
